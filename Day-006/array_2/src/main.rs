// Array elements
// An array is synonymous to a Python list
// Elements in an array are accessed similar to Python lists
//

fn main() {
    let array_2: [i32; 4] = [1, 2, 3, 4];
    println!("Full array: {:?}\n", array_2);
    println!("Ist element: {:?}\n", array_2[0]);
    println!("IInd element: {:?}\n", array_2[1]);
    println!("IIIrd element: {:?}\n", array_2[2]);
    println!("IVth element: {:?}\n", array_2[3]);
}
